export async function getStreamToken() {
  try {
    const response = await axiosInstance.get("/chat/token");
    return response.data;
  } catch (error) {
    console.error("Error fetching Stream token:", error);
    // Re-throw with more context
    if (error.response) {
      // Server responded with error status
      throw new Error(error.response.data?.message || `Failed to get chat token: ${error.response.status}`);
    } else if (error.request) {
      // Request was made but no response received
      throw new Error("Unable to reach server. Please check if backend is running.");
    } else {
      // Something else happened
      throw new Error(error.message || "Failed to get chat token");
    }
  }
}